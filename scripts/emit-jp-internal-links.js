// Emits src/lib/jpInternalLinks.ts — the role list per job-placement industry
// used to build hub -> spoke internal links on each industry hub page.
//
// Source of truth for WHICH pages exist: the actual role page directories under
// src/app/services/job-placement/<industry>/<role>. Display names are pulled
// from the same role data the page generator uses (scripts/data/*). Any role
// directory without a name entry falls back to a title-cased slug.
//
// Run: node scripts/emit-jp-internal-links.js
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const BASE = path.join(ROOT, 'src/app/services/job-placement');

function loadOpt(p) { try { return require(p); } catch (e) { return {}; } }

const JP_ROLES = loadOpt('./data/jp-roles.js');
const INLINE_ROLES = loadOpt('./data/jp-roles-inline.js');
const PACKS = ['./data/jp-pack-tier1.js', './data/jp-pack-tier2.js', './data/jp-pack-tier3.js'].map(loadOpt);
const PACK_ROLES = Object.assign({}, ...PACKS.map((p) => p.roles || {}));
const ALL_ROLES = Object.assign({}, JP_ROLES, PACK_ROLES, INLINE_ROLES);

const nameBySlug = {};
for (const [industry, roles] of Object.entries(ALL_ROLES)) {
  nameBySlug[industry] = nameBySlug[industry] || {};
  for (const r of roles) nameBySlug[industry][r.slug] = r.name;
}

function titleCase(slug) {
  return slug.split('-').map((w) => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

const industries = fs.readdirSync(BASE, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const out = {};
for (const industry of industries) {
  const dir = path.join(BASE, industry);
  const roleDirs = fs.readdirSync(dir, { withFileTypes: true })
    .filter((d) => d.isDirectory() && d.name !== '[city]')
    .map((d) => d.name)
    .sort();
  if (!roleDirs.length) continue;
  out[industry] = roleDirs.map((slug) => ({
    slug,
    name: (nameBySlug[industry] && nameBySlug[industry][slug]) || titleCase(slug),
  }));
}

const header = `// AUTO-GENERATED by scripts/emit-jp-internal-links.js — do not hand-edit.
// Role pages that exist under each job-placement industry, used to build
// hub -> spoke internal links on the industry hub pages. City spokes come
// from jpMetros in ./jpGridData.

export type JpRole = { slug: string; name: string };

export const jpRolesByIndustry: Record<string, JpRole[]> = ${JSON.stringify(out, null, 2)};
`;

const target = path.join(ROOT, 'src/lib/jpInternalLinks.ts');
fs.writeFileSync(target, header);
console.log('Wrote ' + target + ' (' + Object.keys(out).length + ' industries)');
