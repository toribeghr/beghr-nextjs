// One-off generator for the pilot I-9 Verification .docx template.
//
// docxtemplater merges values into an existing .docx that carries {placeholders}
// in its body. This script assembles that source template so the file lives in the
// repo as a real, editable Word document. Re-run with:
//
//   node scripts/build-i9-template.mjs
//
// Placeholders (docxtemplater default {..} delimiters): employeeName, employerName,
// hireDate, state. Keep these in sync with the `fields` in src/lib/templates.ts.

import PizZip from 'pizzip';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '..', 'src', 'lib', 'template-files', 'i9-verification.docx');

// Word paragraph helper. `text` may contain {placeholders}; docxtemplater reads them.
const p = (text, opts = {}) => {
  const { bold = false, size = 22, spacingAfter = 120 } = opts; // size is half-points
  const rpr = `<w:rPr>${bold ? '<w:b/>' : ''}<w:sz w:val="${size}"/><w:szCs w:val="${size}"/></w:rPr>`;
  return (
    `<w:p><w:pPr><w:spacing w:after="${spacingAfter}"/></w:pPr>` +
    `<w:r>${rpr}<w:t xml:space="preserve">${text}</w:t></w:r></w:p>`
  );
};

const body =
  p('Form I-9, Employment Eligibility Verification', { bold: true, size: 32, spacingAfter: 240 }) +
  p(
    'This worksheet records the information used to complete the U.S. Citizenship and ' +
      'Immigration Services (USCIS) Form I-9 for the employee named below. It is a preparation ' +
      'aid only and does not replace the official USCIS Form I-9.',
    { size: 20, spacingAfter: 240 }
  ) +
  p('Employee', { bold: true, size: 26 }) +
  p('Employee full name: {employeeName}') +
  p('Date of hire (first day of employment): {hireDate}') +
  p('State of employment: {state}', { spacingAfter: 240 }) +
  p('Employer', { bold: true, size: 26 }) +
  p('Employer / business name: {employerName}', { spacingAfter: 240 }) +
  p('Attestation', { bold: true, size: 26 }) +
  p(
    'By completing this worksheet, {employerName} confirms it will examine the employee’s ' +
      'original identity and work-authorization documents within three business days of the ' +
      'hire date shown above, in accordance with federal law.',
    { size: 20, spacingAfter: 240 }
  ) +
  p('Employer representative signature: _______________________________', { spacingAfter: 120 }) +
  p('Date: _______________________________', { spacingAfter: 240 }) +
  p(
    'Generated by Business Executive Group (beghr.com). Review with qualified counsel before use.',
    { size: 16 }
  );

const documentXml =
  '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
  '<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">' +
  `<w:body>${body}` +
  '<w:sectPr><w:pgSz w:w="12240" w:h="15840"/>' +
  '<w:pgMar w:top="1440" w:right="1440" w:bottom="1440" w:left="1440" w:header="720" w:footer="720" w:gutter="0"/>' +
  '</w:sectPr></w:body></w:document>';

const contentTypes =
  '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
  '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">' +
  '<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>' +
  '<Default Extension="xml" ContentType="application/xml"/>' +
  '<Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>' +
  '</Types>';

const rootRels =
  '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>' +
  '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">' +
  '<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>' +
  '</Relationships>';

const zip = new PizZip();
zip.file('[Content_Types].xml', contentTypes);
zip.file('_rels/.rels', rootRels);
zip.file('word/document.xml', documentXml);

const buf = zip.generate({ type: 'nodebuffer', compression: 'DEFLATE' });
writeFileSync(OUT, buf);
console.log('Wrote', OUT, `(${buf.length} bytes)`);

// Also emit a base64-embedded TS companion. The serverless generate route decodes this
// instead of reading the .docx from disk — no fs / output-file-tracing dependency, which
// keeps the file reliably bundled into the function on Vercel. The .docx above stays the
// human-editable source of truth; re-run this script after editing it to refresh both.
const B64_OUT = join(__dirname, '..', 'src', 'lib', 'template-files', 'i9-verification.docx.base64.ts');
const b64 = buf.toString('base64');
const tsHeader =
  '// AUTO-GENERATED by scripts/build-i9-template.mjs — do not edit by hand.\n' +
  '// Base64 of src/lib/template-files/i9-verification.docx (the merge source).\n';
writeFileSync(B64_OUT, `${tsHeader}export const I9_VERIFICATION_DOCX_BASE64 =\n  '${b64}';\n`);
console.log('Wrote', B64_OUT, `(${b64.length} b64 chars)`);
