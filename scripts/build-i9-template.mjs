// Embeds the official USCIS Form I-9 PDF as a base64 TS companion.
//
// The serverless generate route decodes this instead of reading the .pdf from disk — no fs /
// output-file-tracing dependency, which keeps the source reliably bundled into the function on
// Vercel (same approach the previous .docx pilot used). Re-run after replacing the source PDF:
//
//   node scripts/build-i9-template.mjs
//
// Source of truth: src/lib/template-files/i9.pdf — the official fillable Form I-9 downloaded
// from uscis.gov (Edition 01/20/25, OMB No. 1615-0047). This script does not modify the form;
// it only re-encodes the committed PDF. To inspect its fillable fields, see
// scripts/introspect-i9-fields.mjs.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PDF = join(__dirname, '..', 'src', 'lib', 'template-files', 'i9.pdf');
const OUT = join(__dirname, '..', 'src', 'lib', 'template-files', 'i9.pdf.base64.ts');

const buf = readFileSync(PDF);
const b64 = buf.toString('base64');
const header =
  '// AUTO-GENERATED by scripts/build-i9-template.mjs — do not edit by hand.\n' +
  '// Base64 of src/lib/template-files/i9.pdf (the official USCIS Form I-9 fill source).\n';
writeFileSync(OUT, `${header}export const I9_PDF_BASE64 =\n  '${b64}';\n`);
console.log('Wrote', OUT, `(${b64.length} b64 chars from ${buf.length} PDF bytes)`);
